    private void borrarCampos() {
        txtContacto1.setText("");
        txtCuit.setText("");
        txtDomicilio.setText("");
        txtEmail.setText("");
        txtLocalidad.setText("");
        txtRazonSocial.setText("");
        txtTel1.setText("");
        txtTel2.setText("");
    }

    private void cargarComboTiposDeSistema() {
        cmbTipoSistema.addItem("CRM GO");
        cmbTipoSistema.addItem("TANGO PUNTO DE VENTA");
        cmbTipoSistema.addItem("TANGO GESTION");
        cmbTipoSistema.addItem("TANGO RESTO");
        cmbTipoSistema.addItem("TANGO ESTUDIOS CONTABLES");
        cmbTipoSistema.addItem("CAPATAZ");
    }

    private void cargarComboTiposDeCliente() {
        cmbTipoCliente.addItem("Abonado");
        cmbTipoCliente.addItem("Potencial");
        cmbTipoCliente.addItem("Mouse");
        cmbTipoCliente.addItem("Paquete horas");
    }

    private boolean validarCampos() {
        if (txtRazonSocial.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "La razón social no puede estar vacía.", "Guardar", JOptionPane.WARNING_MESSAGE);
            txtRazonSocial.setText("");
            txtRazonSocial.requestFocus();
            return false;
        }
        return true;
    }
    
